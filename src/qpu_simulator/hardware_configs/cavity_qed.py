# Cavity QED Hardware Configuration
#
# Parameters for cavity-based quantum systems.
#
# Cavity types:
#   - Fabry-Perot (macroscopic mirrors)
#   - Fiber cavities
#   - Photonic crystal cavities
#   - Microtoroid resonators
#
# Atom types:
#   - Neutral atoms (Rb, Cs)
#   - Ions
#   - Quantum dots (semiconductor cavities)
#
# Key Parameters:
#
# Cavity properties:
#   - Quality factor Q
#   - Decay rate κ
#   - Mode volume
#   - Free spectral range
#
# Atom-cavity coupling:
#   - Coupling strength g
#   - Cooperativity C = g²/(κγ)
#   - Purcell factor
#
# Decoherence:
#   - Atomic spontaneous emission γ
#   - Cavity loss κ
#   - Dephasing from vibrations
#
# Operation modes:
#   - Strong coupling (g > κ, γ)
#   - Dispersive regime
#   - Bad cavity limit
#
# Example configurations:
#   - Rempe group (MPQ)
#   - Kimble group (Caltech, historical)
#   - Ion trap cavities
