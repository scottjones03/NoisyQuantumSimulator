# Trapped Ion Micro-Physics Models
#
# This subpackage contains micro-physics models for all trapped ion variants.
#
# Submodules:
#   - qccd: Quantum Charge-Coupled Device architecture (segmented traps)
#   - penning: Penning trap arrays with rotating crystals
#   - rf_paul: RF Paul trap models with micromotion
#
# Common physics across ion platforms:
#   - Motional mode structure (axial, radial, collective)
#   - Mølmer-Sørensen (MS) gates
#   - Geometric phase gates
#   - Ion-ion Coulomb interaction
#   - Sympathetic cooling
