# QCCD Motional Mode Model
#
# Models for collective motional modes in ion chains.
#
# Physics included:
#   - Normal mode calculation for N-ion chain
#   - Mode frequencies vs trap parameters
#   - Mode heating rates
#   - Spectral crowding with increasing ion number
#   - Mode participation vectors
#
# Inputs (Hardware Parameters):
#   - Trap frequencies
#   - Number of ions
#   - Ion masses
#   - Anomalous heating rate
#
# Outputs:
#   - Mode frequencies and participation vectors
#   - Heating rate per mode
#   - Mode spacing (for crowding analysis)
#   - Recommended gate modes
