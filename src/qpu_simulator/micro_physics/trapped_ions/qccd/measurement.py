# QCCD Measurement Model
#
# Fluorescence-based state readout for trapped ions.
#
# Physics included:
#   - State-dependent fluorescence (bright/dark)
#   - Photon counting statistics
#   - Crosstalk in multi-ion readout
#   - Shelving techniques for improved fidelity
#
# Inputs (Hardware Parameters):
#   - Detection laser parameters
#   - Collection efficiency
#   - Camera/PMT characteristics
#   - Ion spacing in detection zone
#
# Outputs:
#   - Measurement confusion matrix
#   - Readout fidelity
#   - Measurement duration
#   - Crosstalk between adjacent ions
