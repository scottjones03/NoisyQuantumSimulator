# Quantum Error Correction Submodule
#
# Implementations of QEC protocols for fault-tolerant simulation.
#
# Supported codes:
#   - Surface code (rotated and unrotated)
#   - Color codes
#   - High-rate LDPC codes
#   - Bacon-Shor codes
#
# Operations:
#   - Transversal gates
#   - Lattice surgery
#   - Magic state injection
#   - Teleportation-based FTQC
#
# Decoders:
#   - Minimum weight perfect matching (MWPM)
#   - Union-find
#   - Neural network decoders
#   - Belief propagation (for LDPC)
